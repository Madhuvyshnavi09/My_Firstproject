using System;
using System.Collections.Generic;
using System.Text;

namespace My_Firstproject.Encapsulation
{
    class Plus_pattern
    {
        static void Main(string[]args)
        {
            
            for ( int i = 1;i<=5;i++)//outer loop
            {
                for(int j=1;j<=5;j++)//inner loop
                
                    if(i==3||j==3)
                    {
                        Console.Write("*");
                    }
                    else
                    {
                        Console.Write(" ");
                    }
                   {
                    Console.WriteLine();
                   }
                 

                    
                    

                
               

            }
        }   
    }
