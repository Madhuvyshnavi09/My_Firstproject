using System;
using System.Collections.Generic;
using System.Text;

namespace My_Firstproject.Looping
{
    class Sample3
    {
        static void Main(string[]args)
        {
            //print 5 to 1
            int i = 5;
            for(; i>=1; i--)
            {
                Console.WriteLine(i);
            }
        }
    }
}
