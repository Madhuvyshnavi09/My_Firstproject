using System;
using System.Collections.Generic;
using System.Text;

namespace My_Firstproject.Nested
{
    class Demo_8
    {
        static void Main(string[]args)
        {
            
            for(char i='D';i<='A';i--)
            {
                for( char j='A';i<=j;j--)
                {
                    Console.Write(j);
                }
                  Console.WriteLine();
            }
        }
    }
}
